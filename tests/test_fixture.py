def generate_matrix(size) -> np.ndarray:
    """
    Generates random square matrix
    :param size: dimension size
    :return: random numpy matrix
    """
    np.random.seed(1)
    return np.random.rand(size, size) - 0.5


def python_binarize_rows(l: np.ndarray) -> np.ndarray:
    """
    Computes row binarization in python (slow)
    :param l: matrix
    :return: binarized matrix
    """

    def int32(x):
        if x > 0xFFFFFFFF:
            raise OverflowError
        if x > 0x7FFFFFFF:
            x = int(0x100000000 - x)
            if x < 2147483648:
                return -x
            else:
                return -2147483648
        return x

    def binarize(n: np.ndarray) -> int:
        result = 0
        for i in range(32):
            sign = n[i] >= 0
            result |= sign << (31 - i)
        return int32(result)

    padded = []
    for row in l:
        new_row = row.tolist()
        if len(row) % 32 != 0:
            new_row += [-1 for _ in range(32 - len(row) % 32)]
        padded.append(np.array(new_row))

    result = []
    for row in padded:
        new_row = []
        for sub_row in [row[i:i + 32] for i in range(0, len(row), 32)]:
            new_row.append(binarize(sub_row))
        result.append(np.array(new_row))

    return np.array(result)
